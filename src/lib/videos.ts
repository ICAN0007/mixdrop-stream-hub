export function getModelProfile(name: string): ModelProfile {
  const fallback = `https://api.dicebear.com/9.x/initials/svg?seed=${encodeURIComponent(name)}&backgroundColor=111111&textColor=ffffff`;
  const base = modelProfiles[name] ?? {
    image: fallback,
    bio: `${name} is a featured model on ExclusiveClips4, known for a refined presence in premium fashion and lifestyle features. Expect a distinctive style and consistently editorial-grade content.`,
  };
  return {
    ...base,
    card: base.card ?? base.image,
    hero: base.hero ?? base.image,
    portrait: base.portrait ?? base.image,
  };
}

export function slugifyModel(name: string): string {
  return name.toLowerCase().replace(/\s+/g, "-").replace(/[^a-z0-9-]/g, "");
}

export function unslugifyModel(slug: string): string | null {
  const target = slug.toLowerCase();
  return modelCodes.find((m) => slugifyModel(m) === target) ?? null;
}

export function getVideoEmbedUrl(src: string): string {
  if (src.startsWith("http://") || src.startsWith("https://")) return src;
  if (src.startsWith("//")) return `https:${src}`;
  return `https://${src.replace(/^\/+/, "")}`;
}

export function getVideoThumbnailUrl(thumb: string): string {
  if (thumb.startsWith("//")) return `https:${thumb}`;
  return thumb;
}

export function formatDuration(seconds: string): string {
  const s = parseInt(seconds, 10);
  const m = Math.floor(s / 60);
  const rem = s % 60;
  return `${m}:${rem.toString().padStart(2, "0")}`;
}

// Returns the list of models for a video. Supports comma/&/+ separated names
// in the `model` field (e.g. "Eliza Ibarra & Mick Blue").
export function getVideoModels(v: { model: string }): string[] {
  return v.model
    .split(/\s*(?:,|&|\+|\/| and )\s*/i)
    .map((m) => m.trim())
    .filter(Boolean);
}

export function videoHasModel(v: { model: string }, name: string): boolean {
  return getVideoModels(v).some((m) => m.toLowerCase() === name.toLowerCase());
}
